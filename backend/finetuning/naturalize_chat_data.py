def _resp(record: dict) -> str:
    return [m["content"] for m in record["messages"] if m["role"] == "assistant"][-1].strip()


def _user(record: dict) -> str:
    return [m["content"] for m in record["messages"] if m["role"] == "user"][0].strip()


def _pair_key(record: dict) -> Tuple[str, str]:
    return (_user(record), _resp(record))


def naturalize_records(records: List[dict]) -> Tuple[List[dict], int]:
    """동일 응답 그룹에 변형 풀을 라운드로빈 적용."""
    by_resp: Dict[str, List[int]] = defaultdict(list)
    for i, r in enumerate(records):
        by_resp[_resp(r)].append(i)

    changed = 0
    for resp, indices in by_resp.items():
        pool = REWRITE_POOLS.get(resp)
        if not pool:
            continue
        for j, idx in enumerate(indices):
            new_text = pool[j % len(pool)]
            if new_text != resp:
                for m in records[idx]["messages"]:
                    if m["role"] == "assistant":
                        m["content"] = new_text
                changed += 1
    return records, changed


def add_missing_pairs(records: List[dict], folder: str) -> Tuple[List[dict], int]:
    pairs = MISSING_PAIRS.get(folder, [])
    if not pairs:
        return records, 0
    seen = {_pair_key(r) for r in records}
    added = 0
    for user_text, assist in pairs:
        key = (user_text.strip(), assist.strip())
        if key in seen:
            continue
        seen.add(key)
        records.append(
            {"messages": [{"role": "user", "content": user_text}, {"role": "assistant", "content": assist}]}
        )
        added += 1
    return records, added


def process_file(path: Path, apply: bool, add_missing: bool) -> None:
    records = json.loads(path.read_text(encoding="utf-8"))
    before = len(records)
    records, n_rewrite = naturalize_records(records)
    n_add = 0
    if add_missing and path.name == "chat.json":
        folder = path.parent.name
        records, n_add = add_missing_pairs(records, folder)
    print(f"  {path.relative_to(DATASETS_DIR)}: rewrite {n_rewrite}, add {n_add} (total {before} → {len(records)})")
    if apply and (n_rewrite or n_add):
        path.write_text(json.dumps(records, ensure_ascii=False, indent=2), encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description="미쿠 chat.json 응답 자연화")
    parser.add_argument("--dry-run", action="store_true", help="저장 없이 변경 건수만 출력")
    parser.add_argument("--apply", action="store_true", help="chat.json 에 반영")
    parser.add_argument("--add-missing", action="store_true", help="보고싶어 등 누락 쌍 추가")
    args = parser.parse_args()
    if not args.apply and not args.dry_run:
        parser.error("--dry-run 또는 --apply 중 하나를 지정하세요.")

    paths = sorted(DATASETS_DIR.glob("*/chat.json"))
    if not paths:
        raise SystemExit(f"chat.json 없음: {DATASETS_DIR}")

    mode = "apply" if args.apply else "dry-run"
    print(f"[{mode}] {len(paths)}개 chat.json 처리")
    total_rewrite = 0
    total_add = 0
    for p in paths:
        records = json.loads(p.read_text(encoding="utf-8"))
        records, n_rewrite = naturalize_records(records)
        n_add = 0
        if args.add_missing:
            records, n_add = add_missing_pairs(records, p.parent.name)
        total_rewrite += n_rewrite
        total_add += n_add
        print(f"  {p.relative_to(DATASETS_DIR)}: rewrite {n_rewrite}, add {n_add}")
        if args.apply and (n_rewrite or n_add):
            p.write_text(json.dumps(records, ensure_ascii=False, indent=2), encoding="utf-8")

    print(f"\n합계: rewrite {total_rewrite}, add {total_add}")
    if args.dry_run:
        print("반영하려면: python finetuning/naturalize_chat_data.py --apply --add-missing")


if __name__ == "__main__":
    main()
